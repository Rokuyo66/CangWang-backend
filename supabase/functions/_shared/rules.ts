export function normalizeQuestion(q: string): string {
  return (q ?? "")
    .toLowerCase()
    .replace(/[\s，。、？！?!．.…~～「」『』()（）:：;；'"”“]/g, "")
    .replace(/(請問|想問|幫我看|幫我算|占一下|問一下|一下)/g, "");
}

