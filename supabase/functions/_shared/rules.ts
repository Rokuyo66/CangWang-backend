export function parseTagged(text: string) {
  const pick = (tag: string) => {
    const m = text.match(new RegExp(`<${tag}>([\\s\\S]*?)</${tag}>`));
    return m ? m[1].trim() : null;
  };
  const sugg = pick("sugg");
  const due = pick("due");
  const reading = text
    .replace(/<sugg>[\s\S]*?<\/sugg>/g, "")
    .replace(/<due>[\s\S]*?<\/due>/g, "")
    .replace(/<cat>[\s\S]*?<\/cat>/g, "")
    .replace(/<digest>[\s\S]*?<\/digest>/g, "")
    .trim();
  return {
    reading,
    suggested: sugg ? sugg.split("|").map((s) => s.trim()).filter(Boolean).slice(0, 3) : [],
    due: due && /^\d{4}-\d{2}-\d{2}$/.test(due) ? due : null,
    category: pick("cat"),
    digest: pick("digest"),
  };
}

